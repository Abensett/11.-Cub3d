#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*dest;
	int		i;
	int		j;
	int		size;

	i = 0;
	j = 0;
	j = ft_strlen(src);
	size = sizeof(*dest);
	dest = malloc(size * (j + 1));
	if (!dest)
		return (NULL);
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
