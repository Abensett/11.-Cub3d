#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int		index;
	char	c;

	index = 0;
	if (s)
	{
		while (s[index] != '\0')
		{
			c = s[index];
			ft_putchar_fd(c, fd);
			index++;
		}
	}
}
